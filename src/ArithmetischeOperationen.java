public class ArithmetischeOperationen {

    public static void main(String[] args) {

        int a = 5;
        int b = 2;

        //Subtraktion
        // int sub = 5 - 2
        System.out.println(a-b);

        //Addition
        // int add = 5 + 2
        System.out.println(a+b);

        //Multiplikation
        int multi = 5 * 2;
        System.out.println(multi);

        //Division
        System.out.println(a/b);

        //Rest von Division = Modulo
        System.out.println(a%b);

        // Mit Ergebnis von vorheriger Rechnung weiterrechnen
        int p = 2 + multi;
        System.out.println(p);


    }
}
